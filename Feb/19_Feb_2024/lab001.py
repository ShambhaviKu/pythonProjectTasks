class person:
    name = "danny"
    age = 29
    id = 123
    phone_number = None

    def talk(self):
        print('I can talk')

    def walk(self):
        print("I can Walk")

    def sleep(self):
        return "I can sleep"

    def anotherf():
        print("I am f(n)")

danny = person()
danny.talk()

john = person()
print(john.sleep())






